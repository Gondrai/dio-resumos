## Fatiamento de String
### Técnica para retornar substrings.
#### São informados o inicio(start), o final(stop) e passo(step): [start: stop[step]].

nome = "Isabela de Gondra Mendonça Pereira"
print(nome[0])
print(nome[:9])
print(nome[10:])
print(nome[10:16])
print(nome[10:16:2])
print(nome[:])
print(nome[::-1])
